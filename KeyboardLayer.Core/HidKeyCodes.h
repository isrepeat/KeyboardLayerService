#pragma once

enum class HidKeyCode {
	// Letters
	A = 0x04, B = 0x05, C = 0x06, D = 0x07,
	E = 0x08, F = 0x09, G = 0x0A, H = 0x0B,
	I = 0x0C, J = 0x0D, K = 0x0E, L = 0x0F,
	M = 0x10, N = 0x11, O = 0x12, P = 0x13,
	Q = 0x14, R = 0x15, S = 0x16, T = 0x17,
	U = 0x18, V = 0x19, W = 0x1A, X = 0x1B,
	Y = 0x1C, Z = 0x1D,

	// Digits
	Digit1 = 0x1E, Digit2 = 0x1F, Digit3 = 0x20, Digit4 = 0x21,
	Digit5 = 0x22, Digit6 = 0x23, Digit7 = 0x24, Digit8 = 0x25,
	Digit9 = 0x26, Digit0 = 0x27,

	// Control keys
	Enter = 0x28,
	Escape = 0x29,
	Backspace = 0x2A,
	Tab = 0x2B,
	Space = 0x2C,
	CapsLock = 0x39,

	// Function keys
	F1 = 0x3A, F2 = 0x3B, F3 = 0x3C, F4 = 0x3D,
	F5 = 0x3E, F6 = 0x3F, F7 = 0x40, F8 = 0x41,
	F9 = 0x42, F10 = 0x43, F11 = 0x44, F12 = 0x45,

	// Arrows
	ArrowRight = 0x4F,
	ArrowLeft = 0x50,
	ArrowDown = 0x51,
	ArrowUp = 0x52,

	// Insert/Delete
	Insert = 0x49,
	Delete = 0x4C,
	Home = 0x4A,
	End = 0x4D,
	PageUp = 0x4B,
	PageDown = 0x4E,

	// Numpad
	NumLock = 0x53,
	NumpadSlash = 0x54,
	NumpadAsterisk = 0x55,
	NumpadMinus = 0x56,
	NumpadPlus = 0x57,
	NumpadEnter = 0x58,
	Numpad1 = 0x59,
	Numpad2 = 0x5A,
	Numpad3 = 0x5B,
	Numpad4 = 0x5C,
	Numpad5 = 0x5D,
	Numpad6 = 0x5E,
	Numpad7 = 0x5F,
	Numpad8 = 0x60,
	Numpad9 = 0x61,
	Numpad0 = 0x62,
	NumpadDot = 0x63,

	// Modifiers (partially abstract)
	LeftControl = 0xE0,
	LeftShift = 0xE1,
	LeftAlt = 0xE2,
	LeftMeta = 0xE3,
	RightControl = 0xE4,
	RightShift = 0xE5,
	RightAlt = 0xE6,
	RightMeta = 0xE7,

	// Non-standard (subject to mapping)
	FnKey = 0xFFFF  // не стандартный HID-код, используется для псевдо-маркировки
};